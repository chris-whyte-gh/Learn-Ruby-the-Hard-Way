#Function Checklist
#1. start function
def method_name
    
end

#2. Function name with only characters and underscore
def function_name
end

#3. Is there an open parenthesis after the function name?
def function_name()
end

#4. Are there unique argument names separated by commas?
def function_name(arg1, arg2, arg3)
end

#5. Are all lines of code indented two spaces within the function
def function_name(arg1, arg2, arg3)
    First line
    Second line
end

#6. Did the function end  with 'end' on same line as def?
def function_name(arg1, arg2, arg3)
    First line
    Second line
end

#Calling a function (or 'run' or 'use')

#Did you call the function by typing its name?
puts function_name

#Did you put parentheses after the argument, with the values?
puts function_name(arg1, arg2, arg3)
